    private void CalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CalcularActionPerformed
        // TODO add your handling code here:
        try{
            double A = Double.parseDouble(TxtAnum.getText());
            double B = Double.parseDouble(TxtBnum.getText());
            double C = Double.parseDouble(TxtCnum.getText());

            double Disc = Ecuacion.Discriminante(A, B, C);

            if(Disc>=0){
                double X1 = Ecuacion.Sol1(A, B, C, Disc);
                double X2 = Ecuacion.Sol2(A, B, C, Disc);
                TxtX1.setText(String.valueOf(X1));
                TxtX2.setText(String.valueOf(X2));
            }
            else if(Disc<0){
                TxtX1.setText("Sin solucion");
                TxtX2.setText("Sin solucion");
            }
        }
        catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Caracteres no validos");
        }
    }//GEN-LAST:event_CalcularActionPerformed

